public class MultiplyFloatNumbers {
      public static void main(String[] args) {
          float num1 = 6.0f;
          float num2 = 62.02f;
          
          float mul = num1*num2;

          System.out.println("multiply "+num1+" and  "+num2+" is: "+ mul);
      }
      
}// time complexity O(1) and space complexity is also O(1)
